public class ChangChar { 
	public static void main(String[] args) {
		System.out.println("北京\t天津\t上海");
		//规定空格
		System.out.println("北京\n天津\n上海");
		//换行
		System.out.println("北京\\天津\\\\上海");
		//斜杠
		System.out.println("我说:\"你有前途\"");
		//双引号
		System.out.println("我说:\'你有前途\'");
		//单引号
		System.out.println("追梦坦克\r长野");
		//回撤
	}
}